#include "all_func.h"

int	is_full(int **tab)
{
	int i;
	int j;
	int count;

	count = 0;
	i = 0;
	while (i < 9)
	{
		j = 0;
		while (j < 9)
		{
			if (tab[i][j] != 0)
				count++;
			j++;
		}
		i++;
	}
	if (count == 81)
		return (1);
	else
		return (0);
}
