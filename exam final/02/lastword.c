#include <unistd.h>

int main(int ac,char **av)
{
	int i;
	int j;

	i = 0;
	if (ac == 2)
	{
		while (av[1][i])
			++i;
		while (av[1][i - 1] && (av[1][i - 1] == ' ' || av[1][i - 1] == '\t'))
			--i;
		while (av[1][i - 1] && (av[1][i - 1] != ' ' && av[1][i - 1] != '\t'))
			--i;
		while (av[1][i] && (av[1][i] != ' ' && av[1][i] != '\t'))
			write(1, &av[1][i++],1);
	}
	write(1, "\n", 1);
	return (0);
}
